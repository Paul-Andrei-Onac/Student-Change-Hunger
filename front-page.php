			<h2 class="about"> About Students Change Hunger</h2>
			<p class="aboutp">Students Change Hunger (SCH) is a friendly competition where schools and youth community groups host food and/or fund drives at their schools to help feed our hungry New Jersey neighbors. Schools compete to donate the most food or funds to fight back against hunger in our state between Monday, September 13, 2021 and Friday, January 14, 2022. <br><br>

			During the 2020/2021 SCH campaign, more than 100 schools participated, donating more than 56,000 pounds of food and $45,000 to benefit their local food banks. In total, these donated resources supported 181,000 meals for neighbors in need. <br><br>

			This program not only helps to meet the need in our communities, but also teaches students the power of advocacy and outreach. Students are the next generation of leaders, and when they come together to support their communities, great things are possible!</p>
